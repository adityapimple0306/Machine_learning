
# 11. Create any two arrays & perform various mathematical operations.
#     i.e. Addition, Substraction, Multiplication, Divide.
import numpy as np

a1 = np.array([10,20,30,40,50])
a2 = np.array([10,20,30,40,50])
print(f"a1 + a2 = {a1 + a2}")
print(f"a1 - a2 = {a1 - a2}")
print(f"a1 * a2 = {a1 * a2}")
print(f"a1 / a2 = {a1 / a2}")

